/** @param {Record<string, object>} pages @param {Record<string, object>} fieldPages */
export function mergeCalcPageFields(pages, fieldPages) {
  const out = { ...pages }
  for (const [key, extra] of Object.entries(fieldPages)) {
    if (out[key]) out[key] = { ...out[key], ...extra }
  }
  return out
}
